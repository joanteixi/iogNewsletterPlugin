<h3>Ajuda de l'editor</h3>

<strong>Per accedir a l'editor clica dos cops sobre el text del newsletter</strong>

<h4>Com fer links?</h4>
<p>Selecciona el text en el que vols fer un link i clica sobre la icona <span class="cke_icon cke_icon_link"></span>. On diu "URL" escriu l'adreça del link.</p>

<h4>Com posar imatges?</h4>
<p>Posa el cursor a on ha d'anar la imatge o selecciona una imatge existent i clica en la icona <span class="cke_icon cke_icon_image"></span>. S'obre una finestreta on es pot escriure
  l'adreça URL de la imatge o bé incorporar al newsletter una imatge de la mediateca. Per fer-ho, clica a "Veure servidor". S'obre la
  mediateca. Amb el botó dret sobre cada imatge pots seleccionar si vols posar la imatge a mida original (tal com la hem pujat al servidor) o
  bé la imatge retocada (imatge mitjana).</p>
